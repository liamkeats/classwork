package ch02;

public class Primitives 
{
	public static void main(String[] args)
	{
		//char, boolean, int, double, float, byte, ect....
		
		//declare an integer always.
		int x;
		
		//System.out.println(x); CAN'T PRINT BEFORE INITIALIZATION
		
		x = 8; //initialize after the fact
		
		System.out.println(x);
		
		int y = 10; //declare and initialize simultaneously
		
		System.out.println(y);
	}
}
